function parseArgs(argv) {
  const parsed = {
    outDir: null,
    force: false,
    help: false
  };

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];

    if (token === "--out") {
      parsed.outDir = argv[index + 1] ?? null;
      index += 1;
      continue;
    }

    if (token === "--force") {
      parsed.force = true;
      continue;
    }

    if (token === "--help" || token === "-h") {
      parsed.help = true;
    }
  }

  return parsed;
}

function getUsageText() {
  return [
    "Usage: user-habit-pipeline-init-registry --out <directory> [--force]",
    "",
    "Generate a minimal project-registry starter directory for user-habit-pipeline.",
    "",
    "Options:",
    "  --out <directory>   Required target directory for the generated starter files.",
    "  --force             Overwrite generated files if they already exist.",
    "  --help, -h          Show this help text."
  ].join("\n");
}

function printUsageAndExit(code, stream = process.stdout) {
  stream.write(`${getUsageText()}\n`);
  process.exit(code);
}

function validateArgs(args) {
  if (!args.outDir || !String(args.outDir).trim()) {
    throw new Error("--out requires a target directory.");
  }
}

function writeTemplateFiles(outDir, force = false) {
  const resolvedOutDir = path.resolve(process.cwd(), outDir);
  fs.mkdirSync(resolvedOutDir, { recursive: true });

  const writtenFiles = [];

  for (const [filename, content] of Object.entries(TEMPLATE_FILES)) {
    const filePath = path.join(resolvedOutDir, filename);
    if (!force && fs.existsSync(filePath)) {
      throw new Error(`Refusing to overwrite existing file: ${filePath}. Use --force to replace generated files.`);
    }

    fs.writeFileSync(filePath, content, "utf8");
    writtenFiles.push(filePath);
  }

  return {
    ok: true,
    out_dir: resolvedOutDir,
    files: writtenFiles
  };
}

function main(argv = process.argv.slice(2)) {
  const args = parseArgs(argv);
  if (args.help) {
    printUsageAndExit(0, process.stdout);
  }

  try {
    validateArgs(args);
    const result = writeTemplateFiles(args.outDir, args.force);
    process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
  } catch (error) {
    process.stderr.write(`${error.message}\n`);
    process.exit(1);
  }
}

if (require.main === module) {
  main();
}

module.exports = {
  getUsageText,
  main,
  parseArgs,
  validateArgs,
  writeTemplateFiles
};
