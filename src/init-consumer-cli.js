function getCanonicalHostDefinition(host) {
    const normalizedHost = host.trim().toLowerCase();
    const definition = HOST_DEFINITIONS[normalizedHost];
    if (!definition) {
        throw new Error(`Unsupported host: ${host}. Use --host node, python, or codex.`);
    }
    if (definition.generatedReadme) {
        return definition;
    }
    return HOST_DEFINITIONS[definition.canonicalHost];
}
function resolveTemplateSourceDir(sourceDirName) {
    return path.join(__dirname, "..", "examples", sourceDirName);
}
function parseArgs(argv) {
    const parsed = {
        host: null,
        outDir: null,
        force: false,
        help: false
    };
    for (let index = 0; index < argv.length; index += 1) {
        const token = argv[index];
        if (token === "--host") {
            parsed.host = argv[index + 1] ?? null;
            index += 1;
            continue;
        }
        if (token === "--out") {
            parsed.outDir = argv[index + 1] ?? null;
            index += 1;
            continue;
        }
        if (token === "--force") {
            parsed.force = true;
            continue;
        }
        if (token === "--help" || token === "-h") {
            parsed.help = true;
        }
    }
    return parsed;
}
