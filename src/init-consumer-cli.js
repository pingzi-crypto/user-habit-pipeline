function writeConsumerTemplateFiles(host, outDir, force = false) {
    const definition = getCanonicalHostDefinition(host);
    const resolvedOutDir = path.resolve(process.cwd(), outDir);
    const templateSourceDir = resolveTemplateSourceDir(definition.sourceDirName);
    if (!fs.existsSync(templateSourceDir)) {
        throw new Error(`Template source directory not found: ${templateSourceDir}`);
    }
    fs.mkdirSync(resolvedOutDir, { recursive: true });
    const writtenFiles = [];
    const generatedReadmePath = path.join(resolvedOutDir, "README.md");
    if (!force && fs.existsSync(generatedReadmePath)) {
        throw new Error(`Refusing to overwrite existing file: ${generatedReadmePath}. Use --force to replace generated files.`);
    }
    fs.writeFileSync(generatedReadmePath, `${definition.generatedReadme}\n`, "utf8");
    writtenFiles.push(generatedReadmePath);
    for (const filename of definition.filenames) {
        const sourcePath = path.join(templateSourceDir, filename);
        const targetPath = path.join(resolvedOutDir, filename);
        if (!fs.existsSync(sourcePath)) {
            throw new Error(`Template source file not found: ${sourcePath}`);
        }
        if (!force && fs.existsSync(targetPath)) {
            throw new Error(`Refusing to overwrite existing file: ${targetPath}. Use --force to replace generated files.`);
        }
        fs.copyFileSync(sourcePath, targetPath);
        writtenFiles.push(targetPath);
    }
    return {
        ok: true,
        host: definition.canonicalHost,
        out_dir: resolvedOutDir,
        files: writtenFiles
    };
}
